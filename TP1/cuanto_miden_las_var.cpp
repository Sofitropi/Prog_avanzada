#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout<<"*******Tipos fundamentales en c++***********"<<endl;
    //size of es los bytes que tiene ese tipo de variable
    cout<<"\t char: \t"<< sizeof(char)<<endl;  
    cout<<"\t short: "<< sizeof(short)<<endl; 
    cout<<"\t int: \t"<< sizeof(int)<<endl; 
    cout<<"\t long: \t"<< sizeof(long)<<endl; 
    cout<<"\t float: "<< sizeof(float)<<endl; 
    cout<<"\t double: "<< sizeof(double)<<endl;   
    cout<<"\t long double: "<< sizeof(long double)<<endl; 
    
    cout<<"********************************************"<<endl;
    
    
    
    return 0;
}
